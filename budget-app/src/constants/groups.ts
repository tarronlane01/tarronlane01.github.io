/**
 * Default Group Constants
 *
 * These constants define the default "ungrouped" group IDs that always exist
 * for accounts and categories. These groups are created automatically when
 * a budget is created and cannot be deleted.
 */

/**
 * Default account group ID for ungrouped accounts.
 * This group always exists and cannot be deleted.
 */
export const UNGROUPED_ACCOUNT_GROUP_ID = 'ungrouped_accounts'

/**
 * Default category group ID for ungrouped categories.
 * This group always exists and cannot be deleted.
 */
export const UNGROUPED_CATEGORY_GROUP_ID = 'ungrouped_categories'

